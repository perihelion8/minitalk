#include "libft.h"

void	ft_striteri(char *s, void (*f)(t_uint, char *))
{
	t_uint	i;

	if (s == NULL || f == NULL)
		return ;
	i = 0;
	while (s[i])
	{
		f(i, s + i);
		i++;
	}
}
